/**
 * Función principal — genera el bloque completo de posicionamiento
 * para inyectar en el system prompt de cualquier ruta de generación.
 */
export function getNoctraPositioningPrompt(idea: string): string {
  // Detecta si la idea menciona algún mercado específico
  const lower = idea.toLowerCase()

  const marketRelevance = {
    real_estate: ['inmobiliaria', 'propiedad', 'bienes raíces', 'renta', 'constructora'],
    education: ['escuela', 'colegio', 'educación', 'modelo educativo'],
    healthcare: ['médico', 'doctor', 'clínica', 'psicólogo', 'terapeuta'],
    legal: ['abogado', 'despacho', 'jurídico', 'legal'],
    entrepreneurship: ['startup', 'emprendedor', 'fundador', 'negocio'],
  }

  let relevantMarketSection = ''
  for (const [market, keywords] of Object.entries(marketRelevance)) {
    if (keywords.some(k => lower.includes(k))) {
      // Extrae solo la sección relevante de NOCTRA_TARGET_MARKETS
      const marketTitles: Record<string, string> = {
        real_estate: 'INMOBILIARIAS',
        education: 'ESCUELAS',
        healthcare: 'PROFESIONISTAS',
        legal: 'PROFESIONISTAS',
        entrepreneurship: 'EMPRENDEDORES',
      }
      const title = marketTitles[market]
      const start = NOCTRA_TARGET_MARKETS.indexOf(`### ${title}`)
      const end = NOCTRA_TARGET_MARKETS.indexOf('\n### ', start + 1)
      if (start !== -1) {
        relevantMarketSection = end !== -1
          ? NOCTRA_TARGET_MARKETS.slice(start, end)
          : NOCTRA_TARGET_MARKETS.slice(start)
      }
      break
    }
  }

  return `
${NOCTRA_BRAND_POSITIONING}

${relevantMarketSection || NOCTRA_TARGET_MARKETS}

${NOCTRA_PRODUCT_TOPICS}

${NOCTRA_TRENDS_CONTEXT}
`
}
